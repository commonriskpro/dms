#!/usr/bin/env node
/**
 * Ensure apps/dealer/node_modules/zod exists so Next.js resolves "zod".
 * Prefer packages/contracts/node_modules/zod (has valid main/index); fallback to root.
 */
const fs = require("fs");
const path = require("path");

const cwd = process.cwd();
const dealerNm = path.join(cwd, "apps", "dealer", "node_modules");
const dealerZod = path.join(dealerNm, "zod");
const contractsZod = path.join(cwd, "packages", "contracts", "node_modules", "zod");
const rootZod = path.join(cwd, "node_modules", "zod");

const sourceZod = fs.existsSync(contractsZod) ? contractsZod : rootZod;
if (!fs.existsSync(sourceZod)) {
  process.exit(0);
}
// Always re-copy so we get contracts' zod (with valid main) after contracts build, not an early root copy
if (fs.existsSync(dealerZod)) {
  try {
    fs.rmSync(dealerZod, { recursive: true });
  } catch (_) {}
}
try {
  if (!fs.existsSync(dealerNm)) {
    fs.mkdirSync(dealerNm, { recursive: true });
  }
  fs.cpSync(sourceZod, dealerZod, { recursive: true });
  console.log("[ensure-dealer-node_modules] copied zod to", dealerZod);
} catch (e) {
  console.warn("[ensure-dealer-node_modules] skip:", e.message);
}
